class Empleado:
    def __init__(self, codigo, nombre, apellido, cedula, direccion, telefono, cargo, profesion):
        self.codigo = codigo
        self.nombre = nombre
        self.apellido = apellido
        self.cedula = cedula
        self.direccion = direccion
        self.telefono = telefono
        self.cargo = cargo
        self.profesion = profesion
