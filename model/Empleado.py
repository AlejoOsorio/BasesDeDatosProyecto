class Empleado:
    def __init__(self, codigoEmpleado, cedulaEmpleado, nombreEmpleado, apellidoEmpleado, direccionResidenciaEmpleado, telefonoEmpleado, correoEmpleado, profesiones: list):
        self.codigoEmpleado = codigoEmpleado
        self.cedulaEmpleado = cedulaEmpleado
        self.nombreEmpleado = nombreEmpleado
        self.apellidoEmpleado = apellidoEmpleado
        self.direccionResidenciaEmpleado = direccionResidenciaEmpleado
        self.telefonoEmpleado = telefonoEmpleado
        self.correoEmpleado = correoEmpleado
        self.profesiones = profesiones
